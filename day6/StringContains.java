package day6;

public class StringContains {
    public static void main(String[] args) {
        String s1 = "Hello";
        System.out.println(s1.contains("e"));
        System.out.println(s1.contains("i"));
        System.out.println(s1.contains("w"));
        System.out.println(s1.contains("h"));
    }
}
