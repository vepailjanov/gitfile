package day6;

public class Stringconcat {
    public static void main(String[] args) {
        String s1 = "Hi";
        System.out.println(s1 + " there");
        System.out.println(s1.concat (" there"));
        System.out.println(s1 + " everybody");
        System.out.println(s1.concat( " everybody"));
        System.out.println(s1.concat(" there"));
        System.out.println(s1.concat(" you"));

    }
}
