package day8;

public class JavaAssignmentOperators {
    public static void main(String[] args) {
        // +=
        int l = 5;
        l += 5;
        System.out.println(l);
        // -=
        int m = 5;
        m -= 5;
        System.out.println(m);
        // *=
        int n = 5;
        n *= 2;
        System.out.println(n);
        // /=
        int o = 6;
        o /= 3;
        System.out.println(o);
        // %=
        int p = 10;
        p %= 3;
        System.out.println(p);


    }


}
