package Day9;

public class JavaComparisonOperators {
    public static void main(String[] args) {
        int a =10;
        int b = 15;
        System.out.println((a == b)); // ==
        System.out.println((a != b)); // !=
        System.out.println((a > b));  // >
        System.out.println((a < b));  // <
        System.out.println((a >= b));  // >=
        System.out.println((a >= b));  // >=
    }
}
