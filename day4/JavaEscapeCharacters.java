package day4;

public class JavaEscapeCharacters {
    public static void main(String[] args){

        // new line
        System.out.println("Hello \n Hi");
        // quote using escape character
        System.out.println("\"");
        //backslash
        System.out.println(" \\");
        //tab
        System.out.println("\tHello");
    }
}
